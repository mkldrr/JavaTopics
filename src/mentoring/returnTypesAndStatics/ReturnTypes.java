package mentoring.returnTypesAndStatics;

import java.util.List;

public class ReturnTypes {




    //TODO  : VOID

    public void addTwoNums(){
        int num1 =10;
        int num2=30;
        System.out.println(num1+num2);

    }

    //======with params

    public void addTwoNums2(int num1, int num2){

        System.out.println(num1+ num2);

    }


    //TODO RETURN


    public int multiply(){
        int num1 =10;
        int num2=30;
        return num1*num2;
    }


    public int multiply2(int num1, int num2){
        return num1*num2;
    }


//TODO  STATIC METHOD



    // Todo : static void

    public static void addTwoNums3(){
        int num1 =10;
        int num2=30;
        System.out.println(num1+num2);

    }

    //======with params

    public static void addTwoNums4(int num1, int num2){

        System.out.println(num1+ num2);

    }

    //TODO STATIC RETURN TYPE Methods


    public static int multiply3(){
        int num1 =10;
        int num2=7;
        return num1*num2;
    }


    public static int multiply4(int num1, int num2){
        return num1*num2;
    }











}
